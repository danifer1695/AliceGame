#include "Buffer.h"


//******************************************************************************************************************************
// Get()
//******************************************************************************************************************************
	
	Buffer& Buffer::Get(){
		
		static Buffer buffer;
		return buffer;
	}

//******************************************************************************************************************************
// add_content()
//******************************************************************************************************************************

	void Buffer::add_contents(const std::string &input){
		
		contents << input;
	}
	
//******************************************************************************************************************************
// get_content()
//******************************************************************************************************************************

	std::string Buffer::get_contents(){
		
		return contents.str();
	}
	
//******************************************************************************************************************************
// clear_buffer()
//******************************************************************************************************************************

	void Buffer::clear_buffer(){
		
		contents.str("");
		contents.clear();
	}
